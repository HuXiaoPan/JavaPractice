public class Variable {
    public static void main(String[] args) {
        int i=1;
        short s = 120;
        /*
        四类把类型
        作用域
        强制转换
        引用类型和值类型 
        算术运算符
        赋值运算符
        比较运算符
        逻辑运算符
        三元运算符
        */
        s =(short)i;
        i = s;
    }
}